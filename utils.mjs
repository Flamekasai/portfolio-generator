export function getContent(language, id) {
    const translations = literals[id];
    if (translations === undefined) {
        console.error(`Literal "${id}" not found`);
        return id;
    }

    const literal = translations[language];
    if (literal === undefined) {
        console.error(`Literal "${id}" not found for language "${language}"`);
        return id;
    }
    return literal;
}
